# this lec covers :  Conditionals (if-else)

'''
=== python supports if,for and while and doesn't support switch and do while === 

the result of condition either True or False 
in python we have :
boolean ==> True or False
int     ==> 0 is False  and any other value is True 
string  ==> ""(which is an empty string or a null) is False , and any string value "Ahmad" is True 
# first control statement ; 
if(conditon)
    # Do something #
elif(condition)
    # Do something #
else
    # Do something #
'''
Name = "Ali"
if Name=="Ahmad":
    print("Your Name is : Ahmad")
elif Name=="Ali":
    print("Your Name is : Ali")
else :
    print("Not Ahmad Not Ali")
